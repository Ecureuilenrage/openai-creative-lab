function storeLanguage(lang) {
  try {
    localStorage.setItem("ocl-language", lang);
  } catch {}
}

function readStoredLanguage() {
  try {
    return localStorage.getItem("ocl-language");
  } catch {
    return null;
  }
}

function bindRevealTargets() {
  const targets = document.querySelectorAll(".reveal:not([data-reveal-bound])");

  if (!revealObserver) {
    targets.forEach((target) => target.classList.add("is-visible"));
    return;
  }

  targets.forEach((target) => {
    target.dataset.revealBound = "true";
    revealObserver.observe(target);
  });
}

function setupRevealObserver() {
  if (!("IntersectionObserver" in window)) {
    revealObserver = null;
    return;
  }

  revealObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) {
          return;
        }

        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      });
    },
    {
      threshold: 0.14,
      rootMargin: "0px 0px -8% 0px",
    },
  );
}

function updateActiveNav(sectionId) {
  document.querySelectorAll(".topnav a").forEach((link) => {
    const target = link.getAttribute("href")?.slice(1);
    link.classList.toggle("is-active", target === sectionId);
  });
}

function setupSectionObserver() {
  if (!("IntersectionObserver" in window)) {
    return;
  }

  const sections = ["soulborn", "tarot", "toolchain", "archive"]
    .map((id) => document.getElementById(id))
    .filter(Boolean);

  const observer = new IntersectionObserver(
    (entries) => {
      const visible = entries
        .filter((entry) => entry.isIntersecting)
        .sort((left, right) => right.intersectionRatio - left.intersectionRatio)[0];

      if (visible?.target?.id) {
        updateActiveNav(visible.target.id);
      }
    },
    {
      threshold: [0.2, 0.45, 0.7],
      rootMargin: "-35% 0px -45% 0px",
    },
  );

  sections.forEach((section) => observer.observe(section));
}

function renderDynamicContent(lang) {
  renderOverview(lang);
  renderPills("#soulborn-worlds", dataset.soulbornWorlds, "world-tag", lang);
  renderPills("#tarot-cards", dataset.tarotCards, "card-tag-pill", lang);
  renderMediaGrid("#soulborn-media", dataset.soulbornMedia, lang);
  renderPlaceholderGrid("#soulborn-placeholders", dataset.soulbornPlaceholders, lang);
  renderMediaGrid("#tarot-media", dataset.tarotMedia, lang);
  renderPlaceholderGrid("#tarot-placeholders", dataset.tarotPlaceholders, lang);
  renderToolGrid(lang);
  renderArchiveGrid(lang);
  bindRevealTargets();
}

function applyLanguage(lang) {
  const safeLang = lang === "en" ? "en" : "fr";
  applyStaticTranslations(safeLang);
  renderDynamicContent(safeLang);
  updateLanguageButtons(safeLang);
  storeLanguage(safeLang);
}

function setupLanguageSwitcher() {
  document.querySelectorAll(".lang-pill").forEach((button) => {
    button.addEventListener("click", () => {
      applyLanguage(button.dataset.lang);
    });
  });
}

function init() {
  setupRevealObserver();
  setupSectionObserver();
  setupLanguageSwitcher();
  applyLanguage(readStoredLanguage() || "fr");
}

init();
